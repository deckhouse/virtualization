package watcher

import (
	"testing"

	metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"

	"github.com/deckhouse/virtualization/api/core/v1alpha2"
)

func TestRequestsByNodeUSBDeviceDeletion(t *testing.T) {
	nodeUSBDevice := &v1alpha2.NodeUSBDevice{
		ObjectMeta: metav1.ObjectMeta{
			OwnerReferences: []metav1.OwnerReference{{Kind: "ResourceSlice", Name: "slice-match"}},
		},
	}

	requests := requestsByNodeUSBDeviceDeletion(nodeUSBDevice)
	if len(requests) != 1 {
		t.Fatalf("expected one request, got %d", len(requests))
	}

	if requests[0].Name != "slice-match" {
		t.Fatalf("expected request for slice-match, got %q", requests[0].Name)
	}
}

func TestRequestsByNodeUSBDeviceDeletionWithInvalidInput(t *testing.T) {
	nodeUSBDevice := &v1alpha2.NodeUSBDevice{}

	requests := requestsByNodeUSBDeviceDeletion(nodeUSBDevice)
	if requests != nil {
		t.Fatalf("expected nil requests, got %v", requests)
	}
}
